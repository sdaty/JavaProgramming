package pilotCoding;

public class JavaIsFun {
    public static void main(String[] args) {
        String s="Java is fun";
        int c= s.charAt(4);

        if(c=='a'){
            System.out.println("A");
        }else if (c==' '){
            System.out.println("B");
        }else {
            System.out.println("C");
        }
        // output = B
    }
}
