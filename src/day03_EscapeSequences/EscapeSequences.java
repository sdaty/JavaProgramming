package day03_EscapeSequences;
/*

\n: starts a new line
\t: starts a new tab
\\: single back slash( \ )
\":

 */
public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Java \tPython \nC# \t\tC++");
        System.out.println(":\\home\\users\\bill");
        System.out.println();
        System.out.print("   /\\");
        System.out.println("  <== \"Click here!\" what is that?");
        System.out.println("  /  \\");
        System.out.println(" /    \\");
        System.out.println("/______\\");

    }
}
